USE inventory;

CREATE TABLE IF NOT EXISTS `products` (
	`id` INT(11) PRIMARY KEY AUTO_INCREMENT UNIQUE,
    `name` VARCHAR(191) NOT NULL,
    `color` VARCHAR(191) NOT NULL
);

CREATE TABLE IF NOT EXISTS `prices` (
	`id` INT(11) PRIMARY KEY AUTO_INCREMENT UNIQUE,
    `value` DECIMAL(10, 2) NOT NULL
);